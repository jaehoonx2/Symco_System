package symco_System;


import java.awt.Checkbox;
import java.awt.CheckboxGroup;
import java.awt.event.ItemListener;
import java.awt.event.ItemEvent;

public class ColleagueCheckbox extends Checkbox implements ItemListener, Login_Colleague {
	private Login_Mediator mediator;
	public ColleagueCheckbox(String caption, CheckboxGroup group, boolean state){	//생성자
		super(caption, group, state);
	}
	public void setMediator(Login_Mediator mediator) {	// Mediator를 저장
		this.mediator = mediator;
	}
	public void setColleagueEnabled(boolean enabled) {	// Mediator에서 유효/무효를 지시
		setEnabled(enabled);
	}
	public void itemStateChanged(ItemEvent e){	//상태가 바뀌면 Mediator에게 통지
		mediator.colleagueChanged();
	}
}
